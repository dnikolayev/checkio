    #lets load our CPU while we have tasks.
    while (len(tasks)):
        #grabbing task
        coords, direction = tasks.pop()
        
        #special case for initial run
        if coords == [1, 1] and direction:
            continue
            
        #checking if this point is not new
        elif coords in processed:
            continue 
        
        #checking if coords are good and cell is ok
